// Helper function to get translation
export const t = (key, language = 'es') => {
  const keys = key.split('.');
  let value = translations[language];
  
  for (const k of keys) {
    if (value && typeof value === 'object') {
      value = value[k];
    } else {
      return key; // Return the key if translation not found
    }
  }
  
  return value || key;
};

